//Q12: Write a program to convert Celsius  to Fahrenheit   .
//i.e: Temperature in degrees Fahrenheit (°F) = (Temperature in degrees Celsius (°C) * 9/5) + 32




void main()
{
    
    double converter_to_celsius = 40;
    double fahrenheit = (converter_to_celsius * 9/5) + 32;
    print("$converter_to_celsius degree celsius is equals to $fahrenheit fahrenheit");
}




//OUTPUT

40 degree celsius is equals to 104 fahrenheit
